import React, { useState } from "react";

const KYC = () => {
  const [idFile, setIdFile] = useState(null);
  const [selfieFile, setSelfieFile] = useState(null);
  const [loading, setLoading] = useState(false);
  const [message, setMessage] = useState("");

  const toBase64 = (file) =>
    new Promise((resolve, reject) => {
      const reader = new FileReader();
      reader.readAsDataURL(file);
      reader.onload = () => resolve(reader.result.split(",")[1]);
      reader.onerror = (error) => reject(error);
    });

  const handleSubmit = async (e) => {
    e.preventDefault();
    if (!idFile || !selfieFile) {
      setMessage("⚠️ Please upload both ID and selfie.");
      return;
    }

    setLoading(true);
    setMessage("");

    try {
      const idBase64 = await toBase64(idFile);
      const selfieBase64 = await toBase64(selfieFile);

      const token = localStorage.getItem("token"); // from login
      const email = localStorage.getItem("email");

      const res = await fetch("/api/kyc/verify", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          ...(token ? { Authorization: `Bearer ${token}` } : {}),
        },
        body: JSON.stringify({
          email,
          idImageBase64: idBase64,
          selfieBase64: selfieBase64,
        }),
      });

      const data = await res.json();
      if (res.ok) {
        setMessage(`✅ ${data.message}`);
      } else {
        setMessage(`❌ ${data.message || data.error}`);
      }
    } catch (err) {
      setMessage(`Error: ${err.message}`);
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="container-p py-6">
      <h2 className="text-xl font-bold mb-4">KYC Verification</h2>
      <form onSubmit={handleSubmit} className="space-y-4">
        <div>
          <label className="block font-medium">Upload ID</label>
          <input
            type="file"
            accept="image/*"
            onChange={(e) => setIdFile(e.target.files[0])}
            className="mt-1 block"
          />
        </div>
        <div>
          <label className="block font-medium">Upload Selfie</label>
          <input
            type="file"
            accept="image/*"
            onChange={(e) => setSelfieFile(e.target.files[0])}
            className="mt-1 block"
          />
        </div>
        <button
          type="submit"
          disabled={loading}
          className="px-4 py-2 bg-green-600 text-white rounded hover:bg-green-700 disabled:opacity-50"
        >
          {loading ? "Verifying..." : "Submit KYC"}
        </button>
      </form>
      {message && <p className="mt-4">{message}</p>}
    </div>
  );
};

export default KYC;