import React, { useState } from "react";
import { useNavigate } from "react-router-dom";

const KYC = ({ token }) => {
  const [idFile, setIdFile] = useState(null);
  const [selfieFile, setSelfieFile] = useState(null);
  const [loading, setLoading] = useState(false);
  const [message, setMessage] = useState("");
  const navigate = useNavigate();

  const handleSubmit = async (e) => {
    e.preventDefault();
    if (!idFile || !selfieFile) {
      setMessage("⚠️ Please upload both ID and selfie.");
      return;
    }

    const formData = new FormData();
    formData.append("id", idFile);
    formData.append("selfie", selfieFile);

    setLoading(true);
    try {
      const res = await fetch("/api/kyc/verify", {
        method: "POST",
        headers: {
          Authorization: `Bearer ${token}`,
        },
        body: formData,
      });

      const data = await res.json();
      if (res.ok) {
        setMessage("✅ KYC verified successfully!");
        setTimeout(() => navigate("/dashboard"), 1500);
      } else {
        setMessage(`❌ Verification failed: ${data.message}`);
      }
    } catch (err) {
      setMessage("❌ Server error. Try again.");
    }
    setLoading(false);
  };

  return (
    <div className="flex justify-center items-center min-h-screen bg-gray-100">
      <form
        onSubmit={handleSubmit}
        className="bg-white p-6 rounded shadow-md w-full max-w-md space-y-4"
      >
        <h2 className="text-xl font-bold text-center">KYC Verification</h2>

        <label className="block">
          Upload Government ID:
          <input
            type="file"
            accept="image/*"
            onChange={(e) => setIdFile(e.target.files[0])}
            className="mt-1 block w-full"
          />
        </label>

        <label className="block">
          Upload Selfie:
          <input
            type="file"
            accept="image/*"
            onChange={(e) => setSelfieFile(e.target.files[0])}
            className="mt-1 block w-full"
          />
        </label>

        <button
          type="submit"
          disabled={loading}
          className="w-full py-2 bg-blue-600 text-white rounded hover:bg-blue-700"
        >
          {loading ? "Verifying..." : "Submit"}
        </button>

        {message && <p className="text-center mt-2">{message}</p>}
      </form>
    </div>
  );
};

export default KYC;